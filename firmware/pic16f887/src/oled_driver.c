static void ssd1306_init_sequence(void) {
    /* Basic init for 128x64 SSD1306 */
    ssd1306_write_cmd(0xAE); /* display off */
    ssd1306_write_cmd(0xD5); ssd1306_write_cmd(0x80); /* clock */
    ssd1306_write_cmd(0xA8); ssd1306_write_cmd(0x3F); /* multiplex 1/64 */
    ssd1306_write_cmd(0xD3); ssd1306_write_cmd(0x00); /* offset */
    ssd1306_write_cmd(0x40); /* start line 0 */
    ssd1306_write_cmd(0x8D); ssd1306_write_cmd(0x14); /* charge pump on */
    ssd1306_write_cmd(0x20); ssd1306_write_cmd(0x00); /* horizontal addressing */
    ssd1306_write_cmd(0xA1); /* segment remap */
    ssd1306_write_cmd(0xC8); /* COM scan dec */
    ssd1306_write_cmd(0xDA); ssd1306_write_cmd(0x12); /* COM pins */
    ssd1306_write_cmd(0x81); ssd1306_write_cmd(0x7F); /* contrast */
    ssd1306_write_cmd(0xD9); ssd1306_write_cmd(0xF1); /* precharge */
    ssd1306_write_cmd(0xDB); ssd1306_write_cmd(0x40); /* vcom detect */
    ssd1306_write_cmd(0xA4); /* resume RAM content */
    ssd1306_write_cmd(0xA6); /* normal display */
    ssd1306_write_cmd(0xAF); /* display on */
}

void oled_select(oled_target_t t) {
    g_addr7 = (t == OLED_T_COUNTER) ? OLED_ADDR_COUNTER : OLED_ADDR_KITCHEN;
}

void oled_select_kitchen(void) { oled_select(OLED_T_KITCHEN); }
void oled_select_counter(void) { oled_select(OLED_T_COUNTER); }

void oled_clear(void) {
    uint8_t page, col;
    for (page = 0; page < 8u; page++) {
        ssd1306_set_page_col(page, 0);
        for (col = 0; col < 128u; col++) {
            ssd1306_write_data_byte(0x00u);
        }
    }
}

static void oled_putc(char c) {
    uint8_t i;
    uint8_t idx;
    if (c < 0x20 || c > 0x7Fu) c = '?';
    idx = (uint8_t)(c - 0x20);
    for (i = 0; i < 5u; i++) ssd1306_write_data_byte(font5x7[idx][i]);
    ssd1306_write_data_byte(0x00u); /* spacing */
}

void oled_print_line(uint8_t line, const char *text) {
    uint8_t col = 0;
    if (line > 7u) line = 7u;

    ssd1306_set_page_col(line, 0);

    /* Clear line */
    for (col = 0; col < 128u; col++) ssd1306_write_data_byte(0x00u);

    ssd1306_set_page_col(line, 0);
    while (text && *text) {
        oled_putc(*text++);
    }
}

static void init_one_oled(oled_target_t t) {
    oled_select(t);
    /* Ensure bus idle high */
    i2c_scl_release();
    i2c_sda_release();
    __delay_ms(10);
    ssd1306_init_sequence();
    oled_clear();
}

void oled_init(void) {
    /* Start with released lines (needs pull-ups) */
    i2c_scl_release();
    i2c_sda_release();

    init_one_oled(OLED_T_KITCHEN);
    init_one_oled(OLED_T_COUNTER);

    oled_select_kitchen();
    oled_print_line(0, "PIC KITCHEN");
    oled_print_line(1, "BOOT");

    oled_select_counter();
    oled_print_line(0, "PIC COUNTER");
    oled_print_line(1, "BOOT");
}

void oled_show_status(const char *msg) {
    /* Show on both screens so beginner always sees link state */
    oled_select_kitchen();
    oled_print_line(0, "MODE:K");
    oled_print_line(1, msg ? msg : "");

    oled_select_counter();
    oled_print_line(0, "MODE:C");
    oled_print_line(1, msg ? msg : "");
}

void oled_show_mode(app_mode_t mode) {
    if (mode == APP_MODE_KITCHEN) {
        oled_select_kitchen();
        oled_print_line(0, "MODE:K");
        oled_print_line(1, "READY");
    } else {
        oled_select_counter();
        oled_print_line(0, "MODE:C");
        oled_print_line(1, "READY");
    }
}

void oled_show_kitchen_event(const char *evt) {
    oled_select_kitchen();
    oled_print_line(0, "KITCHEN EVT");
    oled_print_line(1, evt ? evt : "");
}

void oled_show_counter_event(const char *evt) {
    oled_select_counter();
    oled_print_line(0, "COUNTER EVT");
    oled_print_line(1, evt ? evt : "");
}

void oled_show_counter_entry(const char *text) {
    oled_select_counter();
    oled_print_line(0, "TABLE:");
    oled_print_line(1, text ? text : "");
}

