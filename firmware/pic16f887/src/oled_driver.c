/* ===== Software I2C (open-drain via TRIS) ===== */
static inline void i2c_scl_release(void) { OLED_SCL_TRIS = 1; }
static inline void i2c_sda_release(void) { OLED_SDA_TRIS = 1; }

static inline void i2c_scl_low(void) { OLED_SCL_PORT = 0; OLED_SCL_TRIS = 0; }
static inline void i2c_sda_low(void) { OLED_SDA_PORT = 0; OLED_SDA_TRIS = 0; }

static inline void i2c_delay(void) { __delay_us(4); } /* ~100kHz-ish at 4MHz */

static void i2c_start(void) {
    i2c_sda_release();
    i2c_scl_release();
    i2c_delay();
    i2c_sda_low();
    i2c_delay();
    i2c_scl_low();
    i2c_delay();
}

static void i2c_stop(void) {
    i2c_sda_low();
    i2c_delay();
    i2c_scl_release();
    i2c_delay();
    i2c_sda_release();
    i2c_delay();
}

static void i2c_write_bit(uint8_t b) {
    if (b) i2c_sda_release();
    else i2c_sda_low();
    i2c_delay();
    i2c_scl_release();
    i2c_delay();
    i2c_scl_low();
    i2c_delay();
}

static uint8_t i2c_read_ack(void) {
    /* release SDA, sample during SCL high */
    uint8_t ack;
    i2c_sda_release();
    i2c_delay();
    i2c_scl_release();
    i2c_delay();
    ack = (uint8_t)(OLED_SDA_PORT ? 0u : 1u); /* 0=ACK pulled low */
    i2c_scl_low();
    i2c_delay();
    return ack;
}

static uint8_t i2c_write_byte(uint8_t b) {
    uint8_t i;
    for (i = 0; i < 8u; i++) {
        i2c_write_bit((uint8_t)((b & 0x80u) != 0u));
        b <<= 1;
    }
    return i2c_read_ack();
}

/* ===== SSD1306 minimal driver ===== */
static uint8_t g_addr7 = OLED_ADDR_KITCHEN;

static void ssd1306_write_cmd(uint8_t cmd) {
    i2c_start();
    (void)i2c_write_byte((uint8_t)(g_addr7 << 1)); /* write */
    (void)i2c_write_byte(0x00u);                   /* control byte: command */
    (void)i2c_write_byte(cmd);
    i2c_stop();
}

static void ssd1306_write_data_byte(uint8_t data) {
    i2c_start();
    (void)i2c_write_byte((uint8_t)(g_addr7 << 1));
    (void)i2c_write_byte(0x40u); /* control byte: data */
    (void)i2c_write_byte(data);
    i2c_stop();
}

static void ssd1306_set_page_col(uint8_t page /*0..7*/, uint8_t col /*0..127*/) {
    ssd1306_write_cmd((uint8_t)(0xB0u | (page & 0x07u)));
    ssd1306_write_cmd((uint8_t)(0x00u | (col & 0x0Fu)));
    ssd1306_write_cmd((uint8_t)(0x10u | ((col >> 4) & 0x0Fu)));
}

